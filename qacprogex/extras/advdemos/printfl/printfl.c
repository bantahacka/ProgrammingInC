#include <stdio.h>

int main (void)
{
	printf ("%+.3e  %f\n", 77.890, 77.890);
	printf ("%+.3e  %f\n", 1234.5678e4, 1234.5678e4);
	printf ("%+.3e  %f\n", .00067891, .00067891);
	printf ("%+.3e  %f\n", 89023.6789, 89023.6789);
	printf ("%+.3e  %f\n", 1.0, 1.0);
	printf ("%+.3e  %f\n", 7777.8, 7777.8);
	printf ("%+.3e  %f\n", -77.0000001, -77.0000001);

    return 0;
}

